# 帮助函数：根据名称查找颜色（简单示例）
def find_color(palette_name, color_name):
    """在指定调色板中通过名称查找颜色"""
    if palette_name in COLOR_PALETTES:
        palette = COLOR_PALETTES[palette_name]
        # 如果是嵌套的 Material Design 结构
        if isinstance(palette, dict):
            for sub_palette in palette.values():
                for color in sub_palette:
                    if color["name"].lower() == color_name.lower():
                        return color
        # 如果是扁平列表结构（如 Traditional Chinese）
        elif isinstance(palette, list):
            for color in palette:
                if color["name"].lower() == color_name.lower():
                    return color
    return None